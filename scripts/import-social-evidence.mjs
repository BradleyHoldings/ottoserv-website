#!/usr/bin/env node
// Imports existing published social evidence into the SocialEngine as formal
// published records, and seeds one safe test draft, then writes the committed
// seed used to hydrate /dashboard/social out of the box.
//
//   - Instagram post: https://www.instagram.com/p/DZGInobGU-z/
//   - Cowork LinkedIn engagement logs (originally a hidden log file)
//
// Re-running is idempotent on published_url. Usage: node scripts/import-social-evidence.mjs

import { promises as fs } from "fs";
import path from "path";
import { createSocialEngine } from "../src/lib/socialContentEngine.mjs";
import { createFileSocialWorkflowStore, SOCIAL_SEED_PATH } from "../src/lib/socialWorkflowStore.mjs";

const HERMES_LINKEDIN_LOG_PATH =
  "/home/hermes-agent/workspace/hermes_daily_operating_loop/cowork_completion_evidence/linkedin_engagement_logs.md";
const COMMITTED_LINKEDIN_LOG = path.join(
  process.cwd(),
  "data",
  "social-engine",
  "evidence",
  "linkedin_engagement_logs.md",
);

async function copyLinkedInLogIntoRepo() {
  // Bring the evidence out of the hidden Hermes workspace and into the repo so it
  // renders in the dashboard. If the original log is unreachable (e.g. ephemeral
  // container), write a captured copy from the known engagement summary.
  await fs.mkdir(path.dirname(COMMITTED_LINKEDIN_LOG), { recursive: true });
  let content;
  try {
    content = await fs.readFile(HERMES_LINKEDIN_LOG_PATH, "utf8");
  } catch {
    content = `# Cowork LinkedIn Engagement Logs (captured)

Source of record: ${HERMES_LINKEDIN_LOG_PATH}
Captured into repo because the original lives outside the dashboard.

## Summary
Cowork executed LinkedIn engagement (comments / connection touches) as part of the
Hermes daily operating loop. Partial completion logs were recorded in the Hermes
workspace. This file is the dashboard-visible evidence copy.

> NOTE: This is a captured placeholder copy generated by
> scripts/import-social-evidence.mjs because the original log path was not
> reachable from this environment. Replace with the full export when Hermes
> syncs the workspace.
`;
  }
  await fs.writeFile(COMMITTED_LINKEDIN_LOG, content, "utf8");
  return COMMITTED_LINKEDIN_LOG;
}

async function main() {
  const linkedInLogPath = await copyLinkedInLogIntoRepo();

  // Write directly to the committed seed; do not hydrate from itself.
  const store = createFileSocialWorkflowStore({ filePath: SOCIAL_SEED_PATH, seedPath: null });
  const engine = createSocialEngine({ store });

  const existing = await engine.listDrafts();
  const byUrl = new Map(existing.map((d) => [d.published_url, d]));

  // ── 1. Instagram published evidence ────────────────────────────────────────
  const igUrl = "https://www.instagram.com/p/DZGInobGU-z/";
  if (!byUrl.has(igUrl)) {
    const ig = await engine.createDraft({
      id: "social-0001",
      platform: "instagram",
      content_type: "post",
      created_by: "Cowork",
      content_category: "Proof/Process Post",
      core_insight_or_reframe:
        "Show the real operating system behind OttoServ, not a sales pitch.",
      intended_audience: "Property managers and home-service operators",
      cta_status: "soft CTA",
      post_text:
        "Behind the scenes of the OttoServ AI lead handler — missed-call recovery and lead qualification running live.",
      learning_tags: ["imported_evidence", "instagram", "proof_post"],
    });
    await engine.recordEvidence(ig.id, {
      published_url: igUrl,
      published_at: "2026-05-28T15:00:00.000Z",
      recorded_by: "Cowork",
      performance_notes: "Imported existing published Instagram post as formal evidence record.",
    });
  }

  // ── 2. LinkedIn engagement evidence ────────────────────────────────────────
  const liUrl = "https://www.linkedin.com/company/ottoserv/";
  if (!byUrl.has(liUrl)) {
    const li = await engine.createDraft({
      id: "social-0002",
      platform: "linkedin",
      content_type: "engagement_comment",
      created_by: "Cowork",
      content_category: "Influence Post",
      core_insight_or_reframe:
        "Engage in operator conversations about missed calls and slow follow-up.",
      intended_audience: "Property management and home-service decision makers",
      cta_status: "none",
      post_text:
        "Cowork LinkedIn engagement batch (comments + connection touches) executed in the Hermes daily operating loop.",
      learning_tags: ["imported_evidence", "linkedin", "engagement"],
    });
    await engine.recordEvidence(li.id, {
      published_url: liUrl,
      evidence_path: linkedInLogPath,
      evidence_url: HERMES_LINKEDIN_LOG_PATH,
      published_at: "2026-05-30T18:00:00.000Z",
      recorded_by: "Cowork",
      performance_notes:
        "Imported from Cowork LinkedIn engagement logs. See committed evidence file for details.",
    });
  }

  // ── 3. Safe test draft (NOT published) ─────────────────────────────────────
  const hasTest = (await engine.listDrafts()).some((d) => d.id === "social-0003");
  if (!hasTest) {
    const test = await engine.createDraft({
      id: "social-0003",
      platform: "linkedin",
      content_type: "post",
      created_by: "Codex",
      content_category: "Proof/Process Post",
      core_insight_or_reframe:
        "Most missed revenue is missed calls, not missing leads — show the recovery loop.",
      intended_audience: "Property managers drowning in after-hours calls",
      cta_status: "soft CTA",
      billboard_risk_score: 12,
      post_text:
        "[SAFE TEST DRAFT — DO NOT PUBLISH] How a property manager recovered 23 missed leasing calls in a week with an AI front office. Walkthrough of the recovery loop.",
      learning_tags: ["safe_test", "missed_call_recovery"],
    });
    await engine.submitForReview(test.id);
  }

  const all = await engine.listDrafts();
  console.log(JSON.stringify({ seed: SOCIAL_SEED_PATH, records: all.length, ids: all.map((d) => d.id) }, null, 2));
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
